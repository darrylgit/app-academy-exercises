def two_d_translate(arr)
  outputArray = []

  arr.each do |innerArray|
    innerArray[1].times do
      outputArray << innerArray[0]
    end
  end

  return outputArray
end

arr_1 = [
  ['boot', 3],
  ['camp', 2],
  ['program', 0]
]

print two_d_translate(arr_1) # => [ 'boot', 'boot', 'boot', 'camp', 'camp' ]
puts

arr_2 = [
  ['red', 1],
  ['blue', 4]
]

print two_d_translate(arr_2) # => [ 'red', 'blue', 'blue', 'blue', 'blue' ]
puts